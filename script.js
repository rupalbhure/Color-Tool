let colorChangeEl = document.getElementById("colorChange");

function onColorGrey(){

    colorChangeEl.style.backgroundColor = "grey";

}

function onColorGreen(){

    colorChangeEl.style.backgroundColor = "green";

}

function onColorBlue(){

    colorChangeEl.style.backgroundColor = "blue";

}

function onColorYellow(){

    colorChangeEl.style.backgroundColor = "yellow";

}